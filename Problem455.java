class Solution455 {
    public int findContentChildren(int[] g, int[] s) {
        int max = 0;
        // sort greed and size
        Arrays.sort(g);
        Arrays.sort(s);
        int i = 0, j = 0;
        while (j < s.length && i < g.length) {
            if (s[j] >= g[i]) {
                i++;
                j++;
                max++;
            } else {
                j++;
            }
        }
        return max;
    }
}